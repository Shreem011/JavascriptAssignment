let grade = 20;

if (grade >= 80) {
  console.log("The grade is", "Distinction");
} else if (grade >= 60 && grade < 80) {
  console.log("The grade is", "First Division");
} else if (grade >= 50 && grade < 60) {
  console.log("The grade is", "Second Division");
} else if (grade >= 40 && grade < 50) {
  console.log("The grade is", "Third Division");
} else {
  console.log("You Fail");
}
